// Package service содержит интерфейсы бизнес-логики для работы с различными типами данных.
//
// В пакете определены контракты (интерфейсы) для работы с сущностями приложения,
// такими как пользователи, учетные записи (Credential), банковские карты,
// текстовые данные (TextData) и бинарные данные (BinaryData).
//
// Реализации этих интерфейсов скрывают детали хранилища (Postgres, локальная ФС, S3 и др.)
// и предоставляют единый API для верхних слоев приложения (например, gRPC, TUI или Web).
//
// Интерфейсы пакета service фокусируются на бизнес-логике и правилах работы с данными,
// а не на механизмах их хранения.
