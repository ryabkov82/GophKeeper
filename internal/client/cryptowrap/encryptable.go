package cryptowrap

// Encryptable описывает поведение сущности,
// которая может быть зашифрована и расшифрована
// с использованием симметричного ключа.
//
// Реализация интерфейса должна обеспечить методы
// Encrypt и Decrypt, принимающие ключ шифрования
// в виде среза байт.
//
// Методы должны возвращать ошибку в случае неудачи
// операций шифрования или дешифрования.
type Encryptable interface {
	// Encrypt шифрует содержимое объекта с использованием ключа key.
	// Возвращает ошибку при неудаче шифрования.
	Encrypt(key []byte) error

	// Decrypt расшифровывает содержимое объекта с использованием ключа key.
	// Возвращает ошибку при неудаче дешифрования.
	Decrypt(key []byte) error
}
